# FRUIT RIPENESS CHECK
# DETERMINE IF A FRUIT IS RIPE ,UNRIPE ,OVERRIPE BASED ON HIS COLOR (eg. Banana : green -> UNRIPE,
# Yellow -> RIPE , Brown -> OVERRIPE)


fruit = "Banana"
color = "Yellow"

if(fruit == "Banana"):
    if color == "Green":
        print("UNRIPE")
    elif color == "Yellow":
        print("RIPE")
    elif color == "Brown":
        print("OVERRIPE")